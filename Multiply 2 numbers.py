"""
Program: multiply two numbers
"""

def main():
    print("This program multiplies two numbers.")
    num1 = input("Enter first number: ")
    num1 = int(num1)
    num2 = input("Enter second number: ")
    num2 = int(num2)
    mul = num1*num2
    print(mul)

if __name__ == '__main__':
    main()
